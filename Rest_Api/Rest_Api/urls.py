from django.contrib import admin
from django.urls import path, include, re_path
from rest_app.views import index, person, PersonalView, Register, PersonalViewClassBased, PersonalGenericView, \
    UserViewSet, GroupViewSet, home
from django.contrib import admin
from rest_framework import routers
from rest_framework.documentation import include_docs_urls



from drf_yasg.views import get_schema_view
from drf_yasg import openapi
from rest_framework import permissions

schema_view = get_schema_view(
    openapi.Info(
        title="Your API",
        default_version='v1',
        description="Your API description",
        terms_of_service="https://www.example.com/terms/",
        contact=openapi.Contact(email="contact@example.com"),
        license=openapi.License(name="Your License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)
admin.autodiscover()
# Routers provide an easy way of automatically determining the URL conf
router = routers.DefaultRouter()
router.register(r'users', UserViewSet)
router.register(r'groups', GroupViewSet)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('index', index ),  #Method based API
    path('person', person ),
    path('classview', PersonalViewClassBased.as_view()),
    path('generic', PersonalGenericView.as_view()),
    path('genriclist', PersonalView.as_view()),
    path('register', Register.as_view()),  # class based API
    path('home', home),
    re_path(r'^docs(?P<format>\.json|\.yaml)$', schema_view.without_ui(cache_timeout=0), name='schema-json'),
    path('docs/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
]

urlpatterns += router.urls

